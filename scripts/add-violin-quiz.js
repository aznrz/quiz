const counts = { easy: 0, medium: 0, hard: 0 };
const questions = RAW.map((row, i) => {
  const [section, options, correct, explanation, prompt] = row;
  counts[section]++;
  const num = String(i + 1).padStart(3, '0');
  return {
    id: `vln-${num}`,
    exam_code: EXAM_CODE,
    section,
    version: 'gen1',
    options,
    correct,
    explanation,
    prompt,
  };
});
